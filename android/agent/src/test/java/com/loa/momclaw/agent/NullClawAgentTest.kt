    fun startServer(modelPath: String) {
        val modelInfo = ModelLoader.LoadResult.Success(modelPath)
 -> File(modelPath).path
        
        // Create engine wrapper
        engine = LlmEngineWrapper(context)
        bridge = LiteRTBridge(context)
 //       Load model from modelInfo.path
 if (!engine.loadModel(modelInfo.path)) {
            throw IllegalArgumentException("Failed to load model from: $modelPath")
        }
        
        // Create server
        val server = embeddedServer(Netty, port = port, module = {
            install(ContentNegotiation) { json(json) }
            install(CORS) {
                anyHost()
            }
            routing {
                // Health check
                get("/health") {
                    healthMonitor.recordRequest()
                    val modelInfo = engine.getModelInfo()
                    call.respond(mapOf(
                        "status" to "healthy",
                        "model_loaded" to modelInfo["loaded"],
                        "model" to modelInfo["name"],
                        "timestamp" to System.currentTimeMillis()
 / 1000
                    ))
                }
                
                // Model info
                get("/v1/models") {
                    healthMonitor.recordRequest()
                    val modelInfo = engine.getModelInfo()
                    call.respond(mapOf(
                        "object" to "list",
                        "data" to listOf(
mapOf(
                            "id" to modelInfo["name"] ?: "unknown",
                            "object" to "model",
                            "created" to System.currentTimeMillis() / 1000,
                            "owned_by" to "google",
                            "path" to (modelInfo["path"] ?: ""),
                            "loaded" to modelInfo["loaded"],
                            "type" to "LiteRT-LM"
                        ))
                    )
                )
            }
            
            // Chat completions
            post("/v1/chat/completions") {
                val request = call.receive<ChatCompletionRequest>()
                healthMonitor.recordRequest()
                
                if (!engine.isReady()) {
                    call.respond(
HttpStatusCode.ServiceUnavailable, mapOf("error" to "Model not loaded"))
                    return@post
                }
                
                val prompt = engine.formatPrompt(request.messages)
                val litertRequest = LiteRTRequest(
                    prompt = prompt,
                    temperature = request.temperature.toFloat(),
                    topP = request.topP.toFloat(),
                    maxTokens = request.maxTokens ?: 2048,
                    stopTokens = request.stop ?: emptyList()
                )
                
                if (request.stream) {
                    // Streaming response
                    call.respondTextWriter(contentType = ContentType.Text.EventStream) {
                        try {
                            engine.generateStreaming(litertRequest).collect { chunk ->


                                val response = ChatCompletionResponse(/* ... */                                
                                write("data: ${json.encodeToString(response)}\n\n")
                                flush()
                                
                                if (chunk.isComplete) {
                                    write("data: [DONE]\n\n")
                                    flush()
                                }
                            }
                        } catch (e: Exception) {
                            call.respond(
HttpStatusCode.InternalServerError, mapOf("error" to "Generation failed"))
                        }
                    } catch (e: Exception) {
                        call.respond(
HttpStatusCode.InternalServerError, mapOf("error" to (e.message ?: "Unknown error"))
                    }
                }
            } else {
                // Non-streaming
                val response = runBlocking { engine.generate(litertRequest) }
                
                val responseId = SSEWriter.generateId()
                val created = SSEWriter.currentTimestamp()
                val response = ChatCompletionResponse(
                    id = responseId,
                    created = created,
                    model = engine.getModelInfo()["name"] as? String ?: "litert",
                    choices = listOf(
                        ChatChoice(
                            index = 0,
                            message = ChatMessage(
                                role = "assistant",
                                content = response.text
                            ),
                            finishReason = "stop"
                        )
                        )
                    ),
                    usage = Usage(
                        promptTokens = response.tokensGenerated,
                        completionTokens = response.tokensGenerated,
                        totalTokens = response.tokensGenerated
                    )
                )
                
                call.respond(response)
            }
        }
        
        bridge.stop()
    }
    
    /**
     * Stop the agent
     */
    fun stop() {
        stop()
 // Cleanup
        bridge.cleanup()
    }
