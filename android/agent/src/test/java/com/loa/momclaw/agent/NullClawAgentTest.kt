## Configuration
- **config/ nullclaw-config.json** - Generated by ConfigurationManager
- **System Prompt** - In Agent/model configuration
- **Memory Backend** - SQLite
- **Tool Access** - Shell, file read/write (with restrictions)

 
 NullClaw uses the LiteRT bridge to interact with models.

## Lifecycle
- **setup()** - Extract binary, generate config
- **start()** - Start process with timeout handling
- **stop()** - Graceful shutdown with proper cleanup
- **monitoring** - Health checks and process diagnostics
- **cleanup()** - Release all resources

 bridgeScope cancel()

