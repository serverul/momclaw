# MomClAW Bridge & Agent Integration

## Overview

This document describes the integration between the LiteRT Bridge and NullClaw Agent.

 Android.

## Architecture

```
┌─────────────────────────────────────────────────────┐
    │     LiteRT Bridge   │   │  NullClaw Agent (Zig)
  │      ↓
│  HTTP: localhost:8080  │   HTTP: localhost:9090  │
 └─────────────────────────────────────────────────────┘
```

## Model Requirements

- **Gemma 3 E4B IT** - downloaded from HuggingFace:
 `litert-community/gemma-3-E4B-it-litertlm`
- **NullClaw Binary** - Extract from assets based device ABI
- **Android 8.7+ (API 28)** - Required
- **Configurable system prompt and settings**
- **SQLite memory backend**

- **Storage**: Device storage (for models)

